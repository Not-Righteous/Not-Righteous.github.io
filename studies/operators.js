/*
Operators:
  1. Assignment Operators
    - Assign values to variables.
      ~ x = y
      ~ x += y
      ~ x -= y
      ~ x *= y
      ~ x /= y
      ~ x %= y
  2. Arithmetic Operators
    - Used to perform arithmetic on numbers.
      ~ Addition ---> +
      ~ Subtraction ---> -
      ~ Multiplication ---> *
      ~ Division ---> /
      ~ Modulus ---> %
      ~ Increment ---> ++
      ~ Decrement ---> --
  3. Comparison Operators
    - Comparing of two values and then evaluating if it is true or false.
      ~ Equal to ---> ==
      ~ Equal value and equal type ---> ===
      ~ Not equal ---> !=
      ~ Not equal value or not equal type ---> !==
      ~ Greater than ---> >
      ~ Less than ---> <
      ~ Greater than or equal to ---> >=
      ~ Less than or equal to ---> <=
      ~ Ternary operator ---> ?
  4. Logical Operators
      ~ Logical and ---> &&
      ~ Logical or ---> ||
      ~ Logical not ---> !
  5. Unary Operators (!, typeOf, -)
    - A unary operator is one that takes a single operand/argument and performs an operation.
      ~ Logical not ---> !
        - Converts to boolean value then negates it.
      ~ typeOf
        - Returns a string which is the type of the operand.
      ~ Unary negation ---> -
        - Tries to convert the operand into a number and negates after.
  6. Ternary Operator (a ? b : c)
    - Only operator that takes 3 operands.
*/