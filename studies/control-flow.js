/*
Control Flow:
  1. If
    - The If statement is used to specify a block of code to be executed, if a specified condition is true.
    - If Example:
      ~ if (condition) {
        //block of code//
      }
  2. Else-If
    - The Else-If statement is used to specify a new condition to test, if the first condition is false.
    - Else-If Example:
      ~ if (condition) {
        //block of code//
      } else if (condition) {
        //block of code//
      }
  3. Else
    - The Else statement is also used to specify a block of code to be executed, if the same condition is false.
    - Else Example:
      ~ if (condition) {
        //block of code//
      } else {
        //block of code//
      }
  4. Switch
    - The Switch statement is used to specify many althernative blocks of code to be executed.
    - Switch Example:
      ~ switch(condition) {
        case a:
          //block of code//
          break;
        default:
          //block of code//
      }
*/